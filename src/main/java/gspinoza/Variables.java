package gspinoza;

// Variables - Store values and can't have more than one value. 
public class Variables {

    public static void main(String[] args) {
        // declaring and initializing variables
        String bird = "Chickadee";
        String habitat = "North America";
        int numberOfSpecies = 7; 
        double averageSize = 3.3; // inches
        char initialLetter = 'C';
        
        // display values
        System.out.println(bird);
        System.out.println(habitat);
        System.out.println(numberOfSpecies);
        System.out.println(averageSize);
        System.out.println(initialLetter);
    }

}
